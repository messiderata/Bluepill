#define RCC ((RCC_TypeDef *)0x40021000) 


#define RCC_APB2ENR_GPIOAEN (1U << 2)
#define RCC_APB2ENR_GPIOBEN (1U << 3)
#define RCC_APB2ENR_GPIOCEN (1U << 4)
#define RCC_APB2ENR_GPIODEN (1U << 5)
#define RCC_APB2ENR_GPIOEEN (1U << 6)
#define RCC_APB2ENR_GPIOFEN (1U << 7)
#define RCC_APB2ENR_GPIOGEN (1U << 8)



#define GPIOA_PCLOCK_EN() (RCC->APB2ENR |= RCC_APB2ENR_GPIOAEN)
#define GPIOB_PCLOCK_EN() (RCC->APB2ENR |= RCC_APB2ENR_GPIOBEN)
#define GPIOC_PCLOCK_EN() (RCC->APB2ENR |= RCC_APB2ENR_GPIOCEN)
#define GPIOD_PCLOCK_EN() (RCC->APB2ENR |= RCC_APB2ENR_GPIODEN)
#define GPIOE_PCLOCK_EN() (RCC->APB2ENR |= RCC_APB2ENR_GPIOEEN)
#define GPIOF_PCLOCK_EN() (RCC->APB2ENR |= RCC_APB2ENR_GPIOFEN)
#define GPIOG_PCLOCK_EN() (RCC->APB2ENR |= RCC_APB2ENR_GPIOGEN)



