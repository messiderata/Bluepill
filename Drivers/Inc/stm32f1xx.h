#define ETH_BASE_ADDRESS (AHBPERIPH_BASE_ADDRESS + 0x8000U)				/* Ethernet MAC */
#define USB_OTG_FS_BASE_ADDRESS (AHBPERIPH_BASE_ADDRESS + 0xFFE8000U)	/* USB OTG FS */
#define FSMC_BASE_ADDRESS (AHBPERIPH_BASE_ADDRESS + 0x50000000U)		/* FSMC */

